public class TreasureChestCapacityException extends Exception {
	
	public TreasureChestCapacityException() {
		super();
	}

	public TreasureChestCapacityException(String message) {
		super(message);
	}

	public TreasureChestCapacityException(String message, Throwable cause) {
		super(message, cause);
	}

	public TreasureChestCapacityException(Throwable cause) {
		super(cause);
	}

	
}