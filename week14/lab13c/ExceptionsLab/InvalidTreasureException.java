public class InvalidTreasureException extends Exception {

	public InvalidTreasureException() {
		super();
	}

	public InvalidTreasureException(String message) {
		super(message);
	}

	public InvalidTreasureException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidTreasureException(Throwable cause) {
		super(cause);
	}

}
